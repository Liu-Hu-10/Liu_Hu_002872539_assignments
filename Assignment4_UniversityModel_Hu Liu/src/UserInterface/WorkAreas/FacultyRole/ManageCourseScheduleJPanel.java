    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblCourseSchedule.getModel();
        model.setRowCount(0);

        Department department = retrieveDepartmentFromBusiness(business);
        if (department != null) {
            for (String semester : department.mastercoursecatalog.keySet()) {
                Object[] row = new Object[]{semester};
                model.addRow(row);
            }
        }
    }
    
        private Department retrieveDepartmentFromBusiness(Business business) {
        String departmentName = "Information Systems";
        for (Department department : business.getDepartments()) {
            if (department.getName().equals(departmentName)) {
                return department;
            }
        }
        return null;
    }
    
