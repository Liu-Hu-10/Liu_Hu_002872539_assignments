    private void btnViewCourseOfferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewCourseOfferActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCourseSchedule.getSelectedRow();
        if (selectedRow >= 0) {
            String selectedSemester = (String) tblCourseSchedule.getValueAt(selectedRow, 0);
            Spring2024CourseOffersJPanel courseOffersJPanel = new Spring2024CourseOffersJPanel(business, selectedSemester, CardSequencePanel);
            CardSequencePanel.add(courseOffersJPanel);
            CardSequencePanel.revalidate();
            CardSequencePanel.repaint();
            ((java.awt.CardLayout) CardSequencePanel.getLayout()).next(CardSequencePanel);
        } else {
            // Show a notification when no row is selected
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a course schedule.");
        }
    }//GEN-LAST:event_btnViewCourseOfferActionPerformed

